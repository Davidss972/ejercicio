
package bloque3.pkg1;

public class While {


    public static void main(String[] args) 
    {
        int i = 0;
        int a;
        while(i <= 5)
        {
        System.out.println(i+"");
        i++;
        }
        System.out.println("Estoy fuera del bucle");
        
        for(a = 0; a <= 5; a++)
        {
        System.out.println(a+"");
        }
        System.out.println("Estoy fuera del bucle for");
    }
    
}
